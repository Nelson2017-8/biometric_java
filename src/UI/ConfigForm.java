    private void btnIniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarActionPerformed
        String urlHabSensor = txtHblSensor.getText();
        String urlRestApi = txtRestApi.getText();
        String keyBrowser = new String(txtKeyBrowser.getPassword());
        String browser = (String) cboBrowser.getSelectedItem();

        Object[] componentes = {txtHblSensor, txtRestApi, txtKeyBrowser, cboBrowser};
        if (validarCampos(componentes) > 0) {
            DesktopNotify.showDesktopMessage("Aviso..!", "Los campos marcados en rojo son obligatorios", DesktopNotify.INFORMATION, 5000L);
            return;
        }
        String action = "add";
        if (cboBrowser.getItemCount() == 1) {
            action = "update";
        }
        boolean r = Utils.setKeyConfig("No index file", urlHabSensor, urlRestApi, keyBrowser, browser, action);
        if (r) {

            DesktopNotify.showDesktopMessage("Aviso..!", "Configuraci√≥n generada o actualizada correctamente."
                    + "\nLa aplicaci√≥n se reiniciara para que la configuraci√≥n tenga efecto",
                    DesktopNotify.INFORMATION, 10000L);
            Timer timer = new Timer();
            TimerTask tarea = new TimerTask() {
                @Override
                public void run() {
                    try {
                        Utils.restartApplication();
                    } catch (IOException ex) {
                        Logger.getLogger(ConfigForm.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            };
            timer.schedule(tarea, 3000, 1);
        } else {
            DesktopNotify.showDesktopMessage("Aviso..!", "Ocurrio un error al crear la configuraci√≥n", DesktopNotify.ERROR, 5000L);
        }
    }//GEN-LAST:event_btnIniciarActionPerformed

