    public void setearTextArea2(){
        File archivo = new File("src/logs/log.log");
        FileReader fr = null;
        try {
            fr = new FileReader(archivo);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(OpenLog.class.getName()).log(Level.SEVERE, null, ex);
        }
        BufferedReader br = new BufferedReader(fr);
        String linea;
        StringBuilder texto = new StringBuilder();
        try {
            while ((linea = br.readLine()) != null) {
                texto.append(linea);
                texto.append("\n");
            }
        } catch (IOException ex) {
            Logger.getLogger(OpenLog.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            br.close();
            fr.close();
        } catch (IOException ex) {
            Logger.getLogger(OpenLog.class.getName()).log(Level.SEVERE, null, ex);
        }
        //System.out.println(texto.toString());
        text_log.setText( texto.toString() );
    }
