    private void exportTxtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_exportTxtActionPerformed
        // TODO add your handling code here:
        String logText = ErrorHandler.read();
        
        // Abrir un diálogo para exportar los datos a un archivo .txt
        JFileChooser fileChooser = new JFileChooser();
        
        JLabel iconLabel = new JLabel(new ImageIcon("/Imagenes/Fingerprint.png"));
        fileChooser.setAccessory(iconLabel);


        FileNameExtensionFilter filter = new FileNameExtensionFilter("Archivos de texto (.txt)", "txt");
        fileChooser.setFileFilter(filter);
        
        int result = fileChooser.showSaveDialog(null);
        if (result == JFileChooser.APPROVE_OPTION) {
            File file = fileChooser.getSelectedFile();
            if (!file.getName().endsWith(".txt")) {
                file = new File(file.getAbsolutePath() + ".txt");
            }
            try (BufferedWriter writer = new BufferedWriter(new FileWriter(file))) {
                writer.write(logText);
                JOptionPane.showMessageDialog(null, "Datos exportados correctamente.");
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(null, "Error al exportar los datos: " + ex.getMessage());
            }
        }
        
    }//GEN-LAST:event_exportTxtActionPerformed

